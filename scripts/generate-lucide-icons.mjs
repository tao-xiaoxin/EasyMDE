  Hash: 'hash',
  GitBranch: 'git-branch',
  History: 'history',
  Image: 'image',
  Info: 'info',
  Italic: 'italic',
  Link2: 'link-2',
  List: 'list',
  ListChecks: 'list-checks',
  ListCollapse: 'list-collapse',
  ListOrdered: 'list-ordered',
  ListTree: 'list-tree',
  LockKeyhole: 'lock-keyhole',
  LayoutGrid: 'layout-grid',
  Maximize: 'maximize',
  Minus: 'minus',
  MoreHorizontal: 'ellipsis',
  Palette: 'palette',
